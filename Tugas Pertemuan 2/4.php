				<h3>Human Resources Director</h3>
				<p>Job Brief Idealnya, Direktur SDM akan mempromosikan dan mengimplementasikan nilai-nilai sumber daya manusia dengan merencanakan dan mengelola program sumber daya manusia. Posisi ini juga memiliki tanggung jawab utama untuk semua aktivitas berbasis orang di dalam organisasi baik dari perspektif operasional maupun strategis. Direktur SDM atau Kepala SDM terlibat dalam banyak bidang, seperti: perencanaan sumber daya dan rekrutmen; latihan; mengelola berbagai masalah ER pada satu waktu dan menjadi titik kontak pertama untuk semua masalah terkait karyawan.</p>
				Tanggung jawab pekerjaan:
				<ol type='a'>
					<li>Bertindak sebagai wajah SDM untuk perusahaan</li>
					<li>Bertanggung jawab untuk pengambilan keputusan tingkat senior dan manajemen harian serta arahan strategis organisasi</li>
					<li>Mengembangkan dan mengimplementasikan agenda tahunan untuk strategi SDM sejalan dengan rencana bisnis. Pastikan rencana SDM mendukung kebutuhan bisnis tetapi juga cukup fleksibel untuk menghadapi perubahan dalam organisasi, seperti dan ketika mereka terjadi</li>
					<li>Memberikan informasi dan laporan tentang data seperti pergantian staf, referensi, biaya per karyawan, jam pelatihan per orang, dll.</li>
					<li>Memberikan saran dan mengelola kasus cuti hamil dan melahirkan. Bertanggung jawab atas perencanaan sumber daya untuk kebutuhan bersalin</li>
					<li>Melakukan analisis dan desain kebutuhan pelatihan dan mengimplementasikan rencana pelatihan dengan masukan dari kepala bisnis. Kelola biaya sesuai anggaran.</li>
					<li>Pastikan semua staf menerima penilaian sesuai dengan kebijakan perusahaan dan memantau masa percobaan</li>
					<li>Koordinasi perekrutan di seluruh perusahaan melalui manajemen tim rekrutmen / tim SDM. Bangun hubungan dengan perekrut, buat konsep singkat, buat wawancara jika perlu. Wawancarai karyawan senior bila perlu</li>
					<li>Pastikan semua Deskripsi Pekerjaan selalu diperbarui, buat JD baru jika diperlukan</li>
					<li>Menangani masalah kinerja atau pengaduan dengan cara yang sesuai hukum dan profesional</li>
					<li>Pantau absensi karena sakit.</li>
					<li>Pastikan semua kebijakan dan prosedur terbaru dan sesuai hukum.</li>
					<li>Memberikan saran sehari-hari kepada manajer lini tentang semua masalah sumber daya manusia: manajemen staf, remunerasi, dll.</li>
					<li>Tinjau semua gaji staf dan buat rekomendasi untuk kenaikan gaji dengan berkonsultasi dengan kepala departemen. Terapkan kenaikan dan promosi apa pun.</li>
					<li>Mempertahankan rencana suksesi untuk semua departemen</li>
					<li>Keterlibatan dalam semua konsultasi redundansi</li>
					<li>Pastikan penggajian selesai secara akurat dan hingga batas waktu</li>
					<li>Melakukan rujukan kesehatan kerja dan memberikan dukungan dan nasihat pastoral kepada karyawan</li>
				</ol>
